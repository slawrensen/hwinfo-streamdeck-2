// Generates native/hwsm/source-id.h with HWSM_SOURCE_ID: a SHA-256 over the
// native source inputs (hwsm.c, hwsm.rc, hwsm-version.h, binding.gyp), so
// getBuildInfo().nativeSourceId identifies exactly which native source built
// a binary. Content-derived only: no timestamps, no repo-wide commit ids, so
// unrelated TypeScript work never changes it. Runs as part of
// `npm run build:native`; a bare node-gyp invocation without it compiles
// with the "unset" fallback.
import { createHash } from "node:crypto";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const nativeDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..", "native", "hwsm");
const inputs = ["hwsm.c", "hwsm.rc", "hwsm-version.h", "binding.gyp"];

const hash = createHash("sha256");
for (const name of inputs) {
	hash.update(name);
	hash.update("\0");
	hash.update(fs.readFileSync(path.join(nativeDir, name)));
	hash.update("\0");
}
const id = hash.digest("hex").slice(0, 16);

const header = `/* Generated by scripts/native-source-id.mjs; do not edit or commit. */
#define HWSM_SOURCE_ID "${id}"
`;
const dest = path.join(nativeDir, "source-id.h");
if (!fs.existsSync(dest) || fs.readFileSync(dest, "utf8") !== header) {
	fs.writeFileSync(dest, header);
}
console.log(`hwsm nativeSourceId: ${id}`);
